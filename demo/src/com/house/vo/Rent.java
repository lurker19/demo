public class Rent {

	private int rid ;
	private int lid ;
	private String rtitle ;
	private int rprice ;
	private int rarea	 ;
	private String rposition ;
	private String rdate ;
	private String rpicture ;
	private String rremarks ;
	
	
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public int getLid() {
		return lid;
	}
	public void setLid(int lid) {
		this.lid = lid;
	}
	public String getRtitle() {
		return rtitle;
	}
	public void setRtitle(String rtitle) {
		this.rtitle = rtitle;
	}
	public int getRprice() {
		return rprice;
	}
	public void setRprice(int rprice) {
		this.rprice = rprice;
	}
	public int getRarea() {
		return rarea;
	}
	public void setRarea(int rarea) {
		this.rarea = rarea;
	}
	public String getRposition() {
		return rposition;
	}
	public void setRposition(String rposition) {
		this.rposition = rposition;
	}
	public String getRdate() {
		return rdate;
	}
	public void setRdate(String rdate) {
		this.rdate = rdate;
	}
	public String getRpicture() {
		return rpicture;
	}
	public void setRpicture(String rpicture) {
		this.rpicture = rpicture;
	}
	public String getRremarks() {
		return rremarks;
	}
	public void setRremarks(String rremarks) {
		this.rremarks = rremarks;
	}
	
	
}
